# 📌 ХЭСЭГ 3: Алдааны Функц & Градиент Уналт (Scratch)
# ============================================================
"""
Алдааны функц (Mean Squared Error - MSE):
    MSE = (1 / N) * sum((y_pred - y) ** 2)

Градиент (Уламжлал) олох томьёо:
    dw = dMSE / dw = (2 / N) * sum((y_pred - y) * X)
    db = dMSE / db = (2 / N) * sum(y_pred - y)

Шинэчлэх дүрэм (Gradient Descent):
    w = w - learning_rate * dw
    b = b - learning_rate * db
"""

# Өгөгдөл бэлтгэх (y = 2 * X + 1 + noise)
np.random.seed(42)
X = np.random.rand(100, 1) * 10  # 0-оос 10 хооронд 100 санамсаргүй цэг
noise = np.random.randn(100, 1) * 1.5  # Санамсаргүй чимээ (noise)
y = 2 * X + 1 + noise  # Бодит хариулт (Үнэн жин w=2, bias b=1)

# Загварын параметрүүд
w = 0.0
b = 0.0
learning_rate = 0.01

def train_model():
    global w, b
    print("\n=== 3. Загвар Сургах Явц (NumPy Scratch) ===")
    print(f"Сургаж эхлэхээс өмнөх параметрүүд: w = {w:.4f}, b = {b:.4f}")
    
    epochs = 100
    n_samples = X.shape[0]
    
    # Эхний параметрүүдийг дахин тэгэлж турших
    w = 0.0
    b = 0.0
    
    print("\nСургаж байна...")
    print(f"{'Epoch':<10}{'Loss (MSE)':<15}{'Weight (w)':<15}{'Bias (b)':<15}")
    print("-" * 55)
    
    for epoch in range(1, epochs + 1):
        # 1. Forward Pass: Таамаглал хийх
        y_pred = X * w + b
        
        # 2. Алдааг (MSE) тооцох
        loss = np.mean((y_pred - y) ** 2)
        
        # 3. Градиент тооцох (Backward Pass)
        dw = (2 / n_samples) * np.sum((y_pred - y) * X)
        db = (2 / n_samples) * np.sum(y_pred - y)
        
        # 4. Параметрийг шинэчлэх (Gradient Descent)
        w -= learning_rate * dw
        b -= learning_rate * db
        
        # 10 epoch тутамд явцыг хэвлэх
        if epoch % 10 == 0 or epoch == 1:
            print(f"{epoch:<10}{loss:<15.4f}{w:<15.4f}{b:<15.4f}")
            time.sleep(0.1)
            
    print("-" * 55)
    print("🎉 Сургалт амжилттай дууслаа!")
    print(f"Сургагдсан параметрүүд:  w = {w:.4f}, b = {b:.4f}")
    print("Бодит үнэн параметрүүд: w = 2.0000, b = 1.0000")


# ============================================================
# 📌 ХЭСЭГ 4: Таамаглал (Prediction) Хийж Шалгах
# ============================================================
def predict_interactive():
    global w, b
    print("\n=== 4. Таамаглал Хийж Үзэх ===")
    if w == 0.0 and b == 0.0:
        print("⚠️  Анхаар: Загварыг хараахан сургаагүй байна! Эхлээд 3-р цэсийг ажиллуулна уу.")
        return
        
    print(f"Одоогийн загвар: y_pred = {w:.4f} * X + {b:.4f}")
    
    while True:
        try:
            val = input("Оролтын утга X-ийг оруулна уу (Гарахын тулд 'q'): ").strip()
            if val.lower() == 'q':
                break
            
            x_input = float(val)
            y_output = w * x_input + b
            
            # Бодит хариулттай харьцуулах (чимээгүй үеийн бодит шулуун y = 2x + 1)
            y_real = 2 * x_input + 1
            
            print(f"🔮 Загварын таамаглал (y_pred) = {y_output:.4f}")
            print(f"📏 Бодит шулууны утга (y_real) = {y_real:.4f}")
            print(f"📉 Зөрүү (Error) = {abs(y_output - y_real):.4f}")
            print("-" * 40)
        except ValueError:
            print("⚠️  Зөвхөн тоон утга эсвэл 'q' оруулна уу!")


